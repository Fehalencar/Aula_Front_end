let a,b;
a=10;//10 número string
b="10";//10 string texto
document.write("Igualdade = "+(a==b)+"<br>");//true
document.write("Idêntico = "+(a===b)+"<br>");//false
document.write("Não igual = "+(a!=b)+"<br>");//false
document.write("Não Idêntico = "+(a!==b)+"<br>");//true
/*
=   | Atribuição/receber - 
==  | Igualdade - true para valores iguais idenpendente do tipo
=== | Identico - false para valores e tipos que não são exatamente iguais
!=  | Não igual - usado para confirmar se ambos são iguais ou diferentes (sempre que tive com "não" significa o inverso)
*/

/*
Sempre que igualdade (==) for verdadeiro, não igual (!=) será falso e vice-versa.
Sempre que identico (===) for verdadeiro, não identico (!==) será falso e vice-versa.
*/

/*
==  |igualdade - valor
=== |identicos - valor e tipo
!=  |diferentes - valor
!== |não identicos valor e tipo
*/